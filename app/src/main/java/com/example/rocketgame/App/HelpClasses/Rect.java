package com.example.rocketgame.App.HelpClasses;

public class Rect {

    public float x;
    public float y;
    public int width;
    public int height;

    public Rect() {}
    public Rect(float x, float y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
